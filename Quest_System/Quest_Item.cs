using System.Collections;
using System.Collections.Generic;
using UnityEngine;

public class Quest_Item : Quest_Objective
{
    [SerializeField]
    Item item;
    

    public Quest_Item(Item item, int count = 1)
    {
        this.item = item;
        this.count = count;
    }

    public Item Item { get => item; set => item = value; }    
}
